package com.github.haseoo.courier.security;
 import com.github.haseoo.courier.models.ClientIndividualModel;
import io.jsonwebtoken.Claims;
import io.jsonwebtoken.JwtException;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.stereotype.Component;
import java.util.Arrays;
import java.util.Date;
import com.github.haseoo.courier.utilities.Constants.EXCEPTION_TRACK_DELIMITER;
@Slf4j
@Component
public class JwtTokenProvider {

@Value("${app.jwtSecret}")
 private  String jwtSecret;

@Value("${app.jwtExpirationInMs}")
 private  int jwtExpirationInMs;


public String generateToken(Authentication authentication){
    UserDetailsImpl userPrincipal = (UserDetailsImpl) authentication.getPrincipal();
    Date now = new Date();
    Date expiryDate = new Date(now.getTime() + jwtExpirationInMs);
    return Jwts.builder().setSubject(Long.toString(userPrincipal.getId())).setIssuedAt(new Date()).setExpiration(expiryDate).signWith(SignatureAlgorithm.HS512, jwtSecret).compact();
}


public Long getUserIdFromJWT(String token){
    Claims claims = Jwts.parser().setSigningKey(jwtSecret).parseClaimsJws(token).getBody();
    return Long.parseLong(claims.getSubject());
}


public String generateTokenToSocialLogin(ClientIndividualModel clientIndividual){
    Claims claims = Jwts.claims().setSubject(clientIndividual.getEmailAddress());
    claims.put("scopes", Arrays.asList(new SimpleGrantedAuthority("ROLE_CLIENT")));
    Date now = new Date();
    Date expiryDate = new Date(now.getTime() + jwtExpirationInMs);
    return Jwts.builder().setSubject(Long.toString(clientIndividual.getId())).setIssuedAt(new Date()).setExpiration(expiryDate).signWith(SignatureAlgorithm.HS512, jwtSecret).compact();
}


public boolean validateToken(String authToken){
    try {
        Jwts.parser().setSigningKey(jwtSecret).parseClaimsJws(authToken);
        return true;
    } catch (JwtException ex) {
        log.error(String.join(EXCEPTION_TRACK_DELIMITER, Arrays.toString(ex.getStackTrace())));
    }
    return false;
}


}